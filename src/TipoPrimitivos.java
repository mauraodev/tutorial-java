public class TipoPrimitivos {

    public static void main(String[] args){

        int idade = 25;

        float valor;
        valor = 1.99f;

        boolean verdadeiroouFalso = false;

        char letraA = 'A';

        System.out.println("A idade: "+idade);
        System.out.println("O valor:" +valor);
        System.out.println("O verdadeiro ou falso: "+verdadeiroouFalso);
        System.out.println("O valor em char:" +letraA);
    }

}
